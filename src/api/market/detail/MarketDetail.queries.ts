import { gql } from "@apollo/client";

export const FETCH_USED_ITEM = gql`
  query fetchUseditem($useditemId: ID!) {
    fetchUseditem(useditemId: $useditemId) {
      _id
      name
      remarks
      contents
      price
      tags
      images
      createdAt
      soldAt
      pickedCount
      useditemAddress {
        address
        lat
        lng
      }
      seller {
        _id
        name
        picture
      }
    }
  }
`;

